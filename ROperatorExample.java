import java.util.*;
public class ROperatorExample {

	public static void main(String[] args) {
     int a=10;
     int b=2;
     int c=4;
     int d=0,e=0;
     if(a>b) {
    	 d=100;
     }
     if(a<b) {
    	 d=34;
     }
     if(a!=c) {
    	 e=100;
     }
     if(a==c) {
    	 e=5;
     }
     if(a<=c) {
    	 e=5;
     }
     System.out.println(a);
     System.out.println(b);
     System.out.println(c);
     System.out.println(d);
     System.out.println(e);
	}

}
